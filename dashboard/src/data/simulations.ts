// ============================================================
// Simulaciones de conversaciones reales con sugerencias del Coach IA
// Cada simulacion es una conversacion turno a turno mostrando:
// - Que dice el cliente (interlocutor)
// - Que responde el usuario
// - Que triggers detecta Maity (sin LLM)
// - Que sugiere el copiloto (con LLM)
// - Si la sugerencia fue correcta/util
// ============================================================

export interface SimTurn {
  id: number;
  speaker: 'interlocutor' | 'user';
  text: string;
  timestamp: string; // MM:SS
}

export interface SimTrigger {
  afterTurnId: number;
  category: string;
  signal: string;
  priority: 'critical' | 'important' | 'soft';
  snippet: string;
}

export interface SimSuggestion {
  afterTurnId: number;
  tip: string;
  category: string;
  subcategory?: string;
  technique?: string;
  priority: 'critical' | 'important' | 'soft';
  confidence: number;
  latencyMs: number;
  verdict: 'correct' | 'useful' | 'neutral' | 'wrong';
  verdictNote: string;
}

export interface Simulation {
  id: string;
  title: string;
  meetingType: 'sales' | 'service' | 'webinar' | 'team_meeting';
  description: string;
  duration: string;
  model: string;
  turns: SimTurn[];
  triggers: SimTrigger[];
  suggestions: SimSuggestion[];
  finalScore: {
    overall: number;
    clarity: number;
    engagement: number;
    structure: number;
    strengths: string[];
    areasToImprove: string[];
  };
}

